package byog.Core;

/**
 * Position class.
 */
public class Position {
    public int x;
    public int y;

    /**
     * Constructor of the Position class
     *
     * @param x the x position
     * @param y the y position
     */
    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }
}

